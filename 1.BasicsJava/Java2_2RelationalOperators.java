public class Java2_2RelationalOperators {
    public static void main(String[] args) {
        // equal Relational Operator (==)
        int A=10;
        int B=10;
        System.out.println(A==B);

        // not Equal to (!=)
        System.out.println(A!=B);

        // less than (<) and greater than (>)
        int a=10;
        int b=5;
        System.out.println(a<b);
        System.out.println(a>b);

        // less than equal to(<=) & greater than equal to (>=)
        System.out.println(a<=b);
        System.out.println(A<=B);
        System.out.println(a>=b);

    }
}
