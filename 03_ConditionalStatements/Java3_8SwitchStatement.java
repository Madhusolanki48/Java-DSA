public class Java3_8SwitchStatement {
    public static void main(String[] args) {
        int num=2;
        switch(num){
            case 1 :
                System.out.println("Mango");
                break;
            case 2 :
                System.out.println("Orange");
                break;
            case 3 :
                System.out.println("Banana");
                break;
            default:
                System.out.println("No fruit");
        }

    }

}
