public class Java2_BitManipulation2 {
    public static void main(String[] args) {
        //Binary One's Compliment

        System.out.println((~5));
        System.out.println((~0));

        //Binary Left Shift
        System.out.println((5<<2));

        //Bitwise Right Shift
        System.out.println(6>>1);
    }
}
